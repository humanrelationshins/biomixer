package org.thechiselgroup.biomixer.client.core.geometry;

public class PointDouble {

    private double x;

    private double y;

    public PointDouble(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        PointDouble other = (PointDouble) obj;
        if (x != other.x) {
            return false;
        }
        if (y != other.y) {
            return false;
        }
        return true;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public PointDouble plus(PointDouble other) {
        return new PointDouble(x + other.getX(), y + other.getY());
    }

    public Point toPointInt() {
        return new Point((int) x, (int) y);
    }

    @Override
    public String toString() {
        return "(" + x + ", " + y + ")";
    }

}
